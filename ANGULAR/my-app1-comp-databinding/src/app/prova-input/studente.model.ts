export class Studente {
  nome: string;
  cognome: string;
  matricola: number;
  materia: string;

  constructor(
    nome: string,
  cognome: string,
  matricola: number,
  materia: string,
  ) {
  this.nome= nome;
  this.cognome= cognome;
  this.matricola= matricola;
  this.materia= materia;
  }
}
